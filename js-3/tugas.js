// Tambah
function tambah (num1, num2){
    return num1 + num2;
}

// Kurang
function kurang (num1, num2){
    return num1-num2;
}

// Perkalian
let kali = (num1, num2) =>
    num1 * num2;

// Pembagian
let bagi = (num1, num2) =>
    num1 / num2;

console.log("Hasil Pertambahan = "+ tambah(4,2));
console.log("Hasil Pengurangan = "+ kurang(4,2));
console.log("Hasil Perkalian = "+ kali(4,2));
console.log("Hasil Pembagian = "+ bagi(4,2));

